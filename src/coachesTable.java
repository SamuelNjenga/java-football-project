    public ArrayList<User8> userList(){
        ArrayList<User8> usersList = new ArrayList<User8>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3306/kabumbu";
            String pass = "";
	    String uname = "root";
            Connection con = DriverManager.getConnection(url,uname,pass);
            String query1 = "SELECT * FROM `coaches`";
            Statement st = con.createStatement();
	    ResultSet rs = st.executeQuery(query1);
            User8 user;
            while(rs.next()){
                
            user = new User8(rs.getString("coachName"),rs.getInt("coachIdNumber"),
                    rs.getString("CoachingSchool"),rs.getInt("schoolNumber"),
                    rs.getString("coachEmailAddress")
            ,rs.getString("gender"));
            
            usersList.add(user);  
            
          }
        }
        catch(Exception e ){
            JOptionPane.showMessageDialog(null, e);
        }
        return usersList;
    }
    public void showUser(){
        ArrayList<User8> list = userList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object []row = new Object[8];
        for(int i = 0;i<list.size();i++){
            row[0] = list.get(i).getCoachName();
            row[1] = list.get(i).getCoachId();
            row[2] = list.get(i).getCoachingSchool();
            row[3] = list.get(i).getSchoolId();
            row[4] = list.get(i).getEmail();
            row[5] = list.get(i).getGender();
            
            model.addRow(row);
        }
    }

